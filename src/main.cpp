#include <renderer.h>


//-----------------------------------------------------------------------------
//      メインエントリーポイントです.
//-----------------------------------------------------------------------------
int main(int argc, char** argv)
{
    r3d::SceneDesc desc = {};
    desc.RenderTimeSec      = 10.0 * 59.9;
    desc.Width              = 2560;
    desc.Height             = 1440;
    desc.FPS                = 59.9;
    desc.AnimationTimeSec   = 10.0;

    r3d::Renderer app(desc);
    app.Run();

    return 0;
}